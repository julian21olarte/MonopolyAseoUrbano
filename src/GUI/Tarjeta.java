    private void jbutcerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbutcerrarActionPerformed
        if(this.pregunta.getCasilla()!=null)
        {
            panel.setMovimientoExtra(1);
            panel.inicio();
        }
        else
        {
            this.panel.actualizarJugador();
            this.player=panel.getFicha();
           
             this.tirarDados.setEnabled(true);
            
             this.labelturno.setText(this.player.getText());
        }
       
        
        this.dispose();
    }//GEN-LAST:event_jbutcerrarActionPerformed

