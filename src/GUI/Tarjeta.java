    public Tarjeta(Color col,JButton tirarDados,JLabel labelturno, Monopoly m,int casilla,int valor,JPanelF panel,JButton player) {
        initComponents();
        this.jtxtarea.setLineWrap(true);
        this.jtxtarea.setWrapStyleWord(true);
        this.m=m;
        this.player=player;
        this.panel=panel;
       
        this.labelturno=labelturno;
        
         if(col.equals(Color.red))
        {
            col=new Color(202,0,0);
        }
        else if(col.equals(Color.blue))
        {
            col=new Color(0,64,128);
        }
        else if(col.equals(Color.green))
        {
            col=new Color(0,100,0);   
        }
        else if(col.equals(Color.yellow))
        {
            col=new Color(185,185,0);   
        } 
        this.tirarDados=tirarDados;
        this.jbutrespuesta.setBackground(col);
        this.jbutcerrar.setBackground(col);
        this.jpanelborde.setBackground(col);
        
        Pregunta cas6=new Pregunta("CARCEL", "HAS CAIDO EN LA CARCEL, DEBES PAGAR $20.000 DE FIANZA PARA PODER SALIR.", null, null);
        Pregunta cas12=new Pregunta("BANCO", "EL BANCO DE LA CIUDAD HA OBSERVADO QUE NO ARROJAS BASURA EN LAS CALLES, POR ESO HA DECIDIDO REGALARTE $20.000.", null, null);
        if(col.equals(Color.white))
        {
            this.jbutrespuesta.setVisible(false);
            col=Color.black;
        }
        
       
        
        this.setLocationRelativeTo(null);
        Random rn=new Random();
        
        String texto="";
        
        
        if(casilla==6)
        {
            this.pregunta=cas6;
            texto=this.pregunta.getPregunta()+"\n\nVALOR: $"+valor;
        }
        else if(casilla==12)
        {
            this.pregunta=cas12;
            texto=this.pregunta.getPregunta()+"\n\nVALOR: $"+valor;
        }
        else if(casilla==18||casilla==24)
        {
            this.pregunta=this.m.getSorpresas()[20];
            texto=this.pregunta.getPregunta();
        }
        else if(casilla==3||casilla==9||casilla==15||casilla==21)
        {
            int num=rn.nextInt(20);
            this.pregunta=this.m.getSorpresas()[num];
            texto=this.pregunta.getPregunta();
        }
        else
        {
            int num=rn.nextInt(49);
            this.pregunta=this.m.getPreguntas()[num];
            texto=this.pregunta.getPregunta()+"\n\nVALOR: $"+valor;
            
        }
        this.jtxtarea.setText(texto);
        
        if(this.pregunta.getRespuesta()==null)
        {
            this.jbutrespuesta.setVisible(false);
        }
        
        
        if(this.pregunta.getCasilla()!=null)
        {
            
            panel.setProxcasilla(this.pregunta.getCasilla());
            panel.setMovimientoExtra(1);
            
        }
        
        this.jlabeltitulo.setText(this.pregunta.getNombre());
        this.jlabeltitulo.setForeground(col);
        
        AudioClip sonido=Applet.newAudioClip(getClass().getResource("/AUDIO/cardslide.wav"));
        sonido.play();
        
    }

    
        
